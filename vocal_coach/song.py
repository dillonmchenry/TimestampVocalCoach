def write_manifest(song_dir: Path, manifest: SongManifest) -> Path:
    """Write ``manifest.json`` into ``song_dir``."""
    out = Path(song_dir) / "manifest.json"
    out.write_text(manifest.model_dump_json(indent=2), encoding="utf-8")
    return out


def load_manifest(song_dir: Path) -> SongManifest:
    """Load ``manifest.json`` from ``song_dir``."""
    path = Path(song_dir) / "manifest.json"
    return SongManifest.model_validate_json(path.read_text(encoding="utf-8"))


