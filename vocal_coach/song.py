SECTIONS_SIDECAR_FILENAME = "sections.yaml"


def load_sections_sidecar(song_dir: Path) -> Optional[list[ReferenceSection]]:
    """Load ``sections.yaml`` next to a song bundle (None if not present).

    Schema (see data/songs/<song_id>/sections.yaml)::

        sections:
          - name: "Chorus 1"
            kind: chorus
            start_s: 40.3
            end_s: 52.7

    Returns ``None`` when the sidecar is absent so callers can fall back to
    the auto ``Phrase N`` sections built from UltraStar phrase breaks.
    """
    song_dir = Path(song_dir)
    sidecar = song_dir / SECTIONS_SIDECAR_FILENAME
    if not sidecar.is_file():
        return None
    try:
        import yaml  # type: ignore
    except Exception:
        return None
    data = yaml.safe_load(sidecar.read_text(encoding="utf-8")) or {}
    entries = data.get("sections") or []
    sections: list[ReferenceSection] = []
    for entry in entries:
        if not isinstance(entry, dict):
            continue
        try:
            sections.append(
                ReferenceSection(
                    name=str(entry["name"]),
                    start_s=float(entry["start_s"]),
                    end_s=float(entry["end_s"]),
                    kind=entry.get("kind"),
                )
            )
        except (KeyError, TypeError, ValueError):
            continue
    return sections or None


def apply_sections_sidecar(
    song_dir: Path,
    annotation: ReferenceAnnotation,
) -> ReferenceAnnotation:
    """If ``song_dir/sections.yaml`` exists, replace annotation.sections with it."""
    sections = load_sections_sidecar(song_dir)
    if sections is None:
        return annotation
    return annotation.model_copy(update={"sections": sections})


def scale_annotation_notes_to_audio(
    annotation: ReferenceAnnotation,
    *,
    target_duration_s: float,
    tolerance: float = 1.15,
) -> ReferenceAnnotation:
    """Compress chart note times when the UltraStar span exceeds the audio length.

    Some community charts (e.g. medley editions) list beats that extend far
    beyond the bundled reference vocal.  Misaligned note windows break global
    offset, octave-shift detection, pitch scoring, and the section ribbon.
    """
    if not annotation.notes:
        return annotation
    chart_end = max(n.end_s for n in annotation.notes)
    if chart_end <= 0 or chart_end <= target_duration_s * tolerance:
        return annotation

    scale = target_duration_s / chart_end
    scaled_notes = [
        n.model_copy(
            update={
                "start_s": n.start_s * scale,
                "end_s": n.end_s * scale,
            }
        )
        for n in annotation.notes
    ]
    word_durs = annotation.word_durs_s
    if word_durs:
        word_durs = [d * scale for d in word_durs]
    ph_durs = annotation.ph_durs_s
    if ph_durs:
        ph_durs = [d * scale for d in ph_durs]

    return annotation.model_copy(
        update={
            "notes": scaled_notes,
            "duration_s": target_duration_s,
            "word_durs_s": word_durs,
            "ph_durs_s": ph_durs,
        }
    )


def write_reference_for_song(
    song_dir: Path,
    annotation: ReferenceAnnotation,
    *,
    indent: int = 2,
) -> tuple[Path, Path]:
    """Write ``reference_annotation.json`` and ``stars_metadata.json`` into a song dir."""
    song_dir = Path(song_dir)

    ref_path = song_dir / "reference_annotation.json"
    ref_path.write_text(annotation.model_dump_json(indent=indent), encoding="utf-8")

    stars_entry: StarsMetadataEntry = annotation.to_stars_metadata(
        wav_fn=str((song_dir / annotation.audio_path).resolve()).replace("\\", "/"),
    )
    stars_meta_path = song_dir / "stars_metadata.json"
    stars_meta_path.write_text(
        json.dumps([stars_entry.model_dump(exclude_none=True)], indent=indent, ensure_ascii=False),
        encoding="utf-8",
    )
    return ref_path, stars_meta_path


# ---------------------------------------------------------------------------
# Helpers for the importer script
# ---------------------------------------------------------------------------


def _wav_metadata(audio_path: Path) -> tuple[int, float]:
    """Return (sample_rate, duration_seconds). Falls back to librosa for mp3s."""
    audio_path = Path(audio_path)
    suffix = audio_path.suffix.lower()
    if suffix in {".wav", ".flac", ".ogg"}:
        info = sf.info(str(audio_path))
        return info.samplerate, info.frames / float(info.samplerate)
    # MP3 / m4a etc. — soundfile can't always read these without libsndfile
    # mp3 support, so fall back to librosa which decodes via audioread.
    import librosa  # noqa: F401  (lazy)

    duration = float(librosa.get_duration(path=str(audio_path)))
    sr = int(librosa.get_samplerate(str(audio_path)))
    return sr, duration


def import_ultrastar_song(
    *,
    song_dir: Path,
    chart_filename: Optional[str] = None,
    reference_vocal: Optional[str] = None,
    instrumental: Optional[str] = None,
    song_id: Optional[str] = None,
    midi_offset: int = 0,
) -> tuple[ReferenceAnnotation, SongManifest]:
    """Build ``reference_annotation.json``, ``stars_metadata.json`` and ``manifest.json``.

    Looks for the chart and audio files inside ``song_dir`` (with optional
    overrides). Returns the in-memory ``ReferenceAnnotation`` and
    ``SongManifest`` after writing them to disk.
    """
    song_dir = Path(song_dir).resolve()
    if not song_dir.is_dir():
        raise FileNotFoundError(f"Song directory not found: {song_dir}")

    # Locate the UltraStar .txt chart.
    if chart_filename is None:
        candidates = [
            p for p in song_dir.glob("*.txt") if p.name.lower() != "license.txt"
        ]
        if len(candidates) != 1:
            raise ValueError(
                f"Expected exactly one UltraStar .txt in {song_dir}, "
                f"found {len(candidates)}: {[c.name for c in candidates]}"
            )
        chart_path = candidates[0]
    else:
        chart_path = song_dir / chart_filename

    chart = parse_ultrastar(chart_path, midi_offset=midi_offset)

    # Resolve audio paths.
    if reference_vocal is None:
        for pattern in ("*reference*vocal*", "*ref*vocal*", "*vocal*", "*reference*"):
            matches = sorted(song_dir.glob(pattern))
            audio_matches = [
                m for m in matches
                if m.suffix.lower() in {".wav", ".mp3", ".flac", ".ogg", ".m4a"}
                and "instrumental" not in m.name.lower()
            ]
            if audio_matches:
                ref_path = audio_matches[0]
                break
        else:
            raise FileNotFoundError(
                f"No reference vocal audio found in {song_dir}; pass --reference-vocal"
            )
    else:
        ref_path = song_dir / reference_vocal

    inst_path: Optional[Path]
    if instrumental is None:
        inst_matches = [
            m for m in song_dir.iterdir()
            if m.is_file()
            and m.suffix.lower() in {".wav", ".mp3", ".flac", ".ogg", ".m4a"}
            and "instrumental" in m.name.lower()
        ]
        inst_path = inst_matches[0] if inst_matches else None
    else:
        inst_path = song_dir / instrumental

    sample_rate, duration_s = _wav_metadata(ref_path)
    if song_id is None:
        # ``Losing My Religion`` -> ``losing-my-religion``
        slug = chart.title or song_dir.name
        song_id = re.sub(r"[^A-Za-z0-9]+", "-", slug).strip("-").lower()

    annotation = reference_from_ultrastar(
        chart,
        sample_id=song_id,
        audio_path=ref_path,
        sample_rate=sample_rate,
        duration_s=duration_s,
        language=chart.language or "English",
    )
    # Sprint 3: if a sections.yaml sidecar exists, use it instead of the
    # auto-derived "Phrase N" sections from the chart's phrase breaks.
    annotation = apply_sections_sidecar(song_dir, annotation)

    write_reference_for_song(song_dir, annotation)

    # Preserve any existing precomputed reference paths so re-importing a
    # song that already had build_song.py run on it doesn't clear them.
    existing_manifest_path = song_dir / "manifest.json"
    existing_ref_pitch = None
    existing_ref_stars = None
    existing_ref_loudness = None
    if existing_manifest_path.is_file():
        try:
            _old = SongManifest.model_validate_json(
                existing_manifest_path.read_text(encoding="utf-8")
            )
            existing_ref_pitch = _old.reference_pitch_path
            existing_ref_stars = _old.reference_stars_path
            existing_ref_loudness = _old.reference_loudness_path
        except Exception:
            pass

    manifest = SongManifest(
        song_id=song_id,
        title=chart.title or song_id,
        artist=chart.artist or "",
        language=chart.language or "English",
        chart_path=chart_path.name,
        reference_vocal_path=ref_path.name,
        instrumental_path=inst_path.name if inst_path else None,
        duration_s=duration_s,
        ultrastar=UltraStarMetadata(
            bpm=chart.bpm,
            gap_ms=chart.gap_ms,
            audio_ref=chart.audio_ref,
            cover=chart.cover,
            background=chart.background,
            edition=chart.edition,
            midi_offset=midi_offset,
        ),
        reference_pitch_path=existing_ref_pitch,
        reference_stars_path=existing_ref_stars,
        reference_loudness_path=existing_ref_loudness,
    )
    write_manifest(song_dir, manifest)
    return annotation, manifest


